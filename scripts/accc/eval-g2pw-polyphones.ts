#!/usr/bin/env tsx
import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import { hanVietReadings, loadHanViet } from "content-pipeline/hanviet";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, "..", "..");
const DEFAULT_POLYPHONE_LOG = join(REPO_ROOT, "out", "polyphone-candidates.jsonl");
const DEFAULT_REPORT = join(REPO_ROOT, "out", "g2pw-eval-report.md");

interface PolyphoneLogRow {
  char: string;
  readings: string[];
  hvAmbiguous?: boolean;
  chosen?: string;
}

function loadPolyphoneChars(logPath: string): Map<string, PolyphoneLogRow> {
  const byChar = new Map<string, PolyphoneLogRow>();
  if (!existsSync(logPath)) return byChar;

  for (const line of readFileSync(logPath, "utf8").split(/\r?\n/)) {
    if (!line.trim()) continue;
    const row = JSON.parse(line) as PolyphoneLogRow;
    if (!row.char) continue;
    byChar.set(row.char, row);
  }
  return byChar;
}

function renderReport(opts: {
  totalChars: number;
  deterministicCorrect: number;
  ambiguousResidual: number;
  chars: string[];
}): string {
  const denom = opts.totalChars || 1;
  const accuracy = (opts.deterministicCorrect / denom) * 100;
  return [
    "# g2pW polyphone eval report (measure only — v1)",
    "",
    "> **g2pW is candidate / to-integrate.** This script does not wire g2pW into the generation path.",
    "",
    "## Dataset",
    `- Polyphone chars observed in readings: **${opts.totalChars}**`,
    `- Source: \`out/polyphone-candidates.jsonl\``,
    "",
    "## Measured accuracy (deterministic hanviet.json path)",
    `- Single-reading deterministic hits: **${opts.deterministicCorrect} / ${opts.totalChars}** (${accuracy.toFixed(1)}%)`,
    `- Ambiguous residual (multi-reading, not auto-picked): **${opts.ambiguousResidual}**`,
    "",
    "## Char set",
    opts.chars.length ? opts.chars.map((c) => `- ${c}`).join("\n") : "- (none logged yet)",
    "",
    "## Recommendation",
    ambiguousResidualLine(opts.ambiguousResidual, accuracy),
    "",
    "_Generated by `scripts/accc/eval-g2pw-polyphones.ts` — eval only, no pipeline integration._",
    "",
  ].join("\n");
}

function ambiguousResidualLine(ambiguous: number, accuracy: number): string {
  if (ambiguous === 0) {
    return "- No ambiguous chars in the current log; re-run after generation waves populate `polyphone-candidates.jsonl`.";
  }
  if (accuracy >= 94) {
    return `- Deterministic lookup covers ${accuracy.toFixed(1)}% of the logged char set; integrate g2pW only for the ${ambiguous} ambiguous residual(s) after a measured pilot.`;
  }
  return `- Deterministic coverage is ${accuracy.toFixed(1)}% on the logged set; prioritize g2pW measurement on the ${ambiguous} ambiguous char(s) before any integration decision.`;
}

function main(): void {
  const logPath = process.argv[2] ?? DEFAULT_POLYPHONE_LOG;
  const reportPath = process.argv.includes("--out")
    ? process.argv[process.argv.indexOf("--out") + 1]!
    : DEFAULT_REPORT;

  const byChar = loadPolyphoneChars(logPath);
  const hanviet = loadHanViet();
  const chars = [...byChar.keys()].sort((a, b) => a.localeCompare(b, "zh-Hant"));

  let deterministicCorrect = 0;
  let ambiguousResidual = 0;

  for (const char of chars) {
    const readings = hanVietReadings(hanviet, char);
    if (readings.length <= 1) {
      deterministicCorrect++;
    } else {
      ambiguousResidual++;
    }
  }

  const report = renderReport({
    totalChars: chars.length,
    deterministicCorrect,
    ambiguousResidual,
    chars,
  });

  writeFileSync(reportPath, report, "utf8");
  console.log(`wrote ${reportPath}`);
  console.log(
    `polyphone chars: ${chars.length}; deterministic: ${deterministicCorrect}; ambiguous: ${ambiguousResidual}`,
  );
}

main();